public class SkipListBound {
    private SkipListBound top, right, bottom, left;

    public SkipListBound() {
    }

    public SkipListBound getTop() {
        return this.top;
    }

    public void setTop(SkipListBound top) {
        this.top = top;
    }

    public SkipListBound getRight() {
        return this.right;
    }

    public void setRight(SkipListBound right) {
        this.right = right;
    }

    public SkipListBound getBottom() {
        return this.bottom;
    }

    public void setBottom(SkipListBound bottom) {
        this.bottom = bottom;
    }

    public SkipListBound getLeft() {
        return this.left;
    }

    public void setLeft(SkipListBound left) {
        this.left = left;
    }
}
